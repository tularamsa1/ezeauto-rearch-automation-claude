            GlobalVariables.time_calc.execution.start()
            logger.debug(f"Execution Timer started in testcase function : {testcase_id}")
            # ------------------------------------------------------------------------------------------------
            app_driver = TestSuiteSetup.initialize_app_driver(testcase_id)
            login_page = LoginPage(app_driver)
            logger.info(f"Logging in the MPOSX application using username : {app_username}")
            login_page.perform_login(app_username, app_password)
            home_page = HomePage(app_driver)
            home_page.check_home_page_logo()
            home_page.wait_for_navigation_to_load()
            logger.debug(f"performing language update from English to Hindi")
            acount_page = AccountPage(app_driver)
            home_page.wait_to_load_today_sales()
            home_page.click_account_menu()
            acount_page.click_on_setting()
            acount_page.click_on_Language()
            acount_page.click_on_hindi_Lang()
            acount_page.click_on_proceed_btn()
            logger.debug(f"completed language update")
            my_reports = home_page.fetch_text_from_my_reports_tab()
            logger.debug("changing to English language")
            home_page.click_on_account_mnu_hindi()
            acount_page.click_on_setting()
            acount_page.click_on_Language()
            acount_page.click_on_eng_Lang()
            acount_page.click_on_proceed_btn()
